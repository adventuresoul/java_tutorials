// import java.lang.*;

// Data types and variables
// Data types -> primitives(single value), non-primitive 
// primitives -> int, double, float, short, byte, char, long, boolean
// non - primitives -> arrays, string

public class Variables {
    public static void main (String[] args) {
        int a = 5;
        double b = 5.5;
        float c = 4.5f;
        long d = 456666334l;
        char e = 'v';
        boolean f = true;

        // formatted output
        System.out.printf("%d, %.2f, %.2f, %d, %c, %b%n", a, b, c, d, e, f);
    }
}
